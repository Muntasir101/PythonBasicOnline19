"""
Those operators will return True or False.
'==' (Equal to)
'!=' (Not Equal to)
'<' (Less than)
'>' (Greater than)
'<=' (Less than or Equal)
'>=' (Greater than or Equal)
"""
print(10 == 10)  # True
print(10 != 10)  # False
print(10 < 20)  # True
print(10 > 20)  # False
print(10 <= 20)  # True
print(10 >= 20)  # False
